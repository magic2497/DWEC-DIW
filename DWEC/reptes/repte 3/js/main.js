function dividirCadena(cadena) {
  var array = cadena.split(",");

  for (var i = 0; i < array.length; i++) {
    document.write(array[i] + " / ");
  }

  document.getElementById("resultat").innerHTML = ` 
 
  for (var i = 0; i < array.length; i++) {
    document.write(array[i] + " / ");
  }
  
  
  `;
}
